            <h2>Tourisme</h2>
            <p>En termes de tourisme, les voyageurs pourront faire de multiples activités, elles sont soumises à des restrictions visant à protéger les sites et à assurer la qualité du service. Les activités se déroulent sur terre ou en pleine mer.</p>
            <p>La croisière polaire : C’est le meilleure moyen de visiter l’archipel, les croisières polaires se font autour de l’île de Spitzberg en passant par la falaise d’Alkefjellet, l’île de la Terre du Nord et le détroit d’Hinlopen. Les croisières se font en petits groupes accompagné de guides polaires, vous apprendrez en détails les informations de l’archipel ainsi que les spécificités de Svalbard.</p>
            <p>Motoneiges et randonnées : Les visiteurs adorent les promenades en motoneige ainsi que les sorties en chiens de traîneau, profitez d’une expérience en grand air unique ! La région vous offre environ 2650 glaciers et monts, une faune et une flore locale à couper le souffle.</p>
            <p>Le secteur touristique contribue à la protection des animaux, les activités sont payantes et l’argent gagné permet de payer des recherches qui permettront la sauvegarde de la nature.</p>
