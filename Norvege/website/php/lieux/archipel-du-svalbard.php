        <h2>L'Archipel et sa Nature</h2>
            <p>La nature jouit de tous ses droits, la température moyenne est de -5,7 degrés, l’archipel compte 170 espèces réussissant parfaitement à s’adapter aux conditions météorologiques. Pour comparer le nord de la Norvège compte 500 espèces, qui elles, bénéficient de conditions plus avantageuses et propices à leur développement.</p>
            <br>
            <p>L’ours polaire est le symbole de l’archipel. L’ours blanc est l’espèce la plus représenté au sein de la faune de Svalbard. En 2010, le territoire comptait environ 1000 ours, ils ont su coloniser et apprivoiser les terres difficiles de l’archipel. Pour protéger les ours, de diverses mesures ont été misent en place visant à lui permettre d’y vivre sereinement. La chasse de l’ours est totalement interdite. On trouve aussi d’autres espèces protégées comme le renard arctique qui était autrefois traqué et le renne de Svalbard qui est une sous-espèce locale.</p>
